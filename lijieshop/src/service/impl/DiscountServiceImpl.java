package service.impl;

import java.util.HashMap;
import java.util.List;
import java.util.Map;

import javax.annotation.Resource;

import org.springframework.stereotype.Service;

import dao.DiscountDao;
import entity.Discount;
import service.DiscountService;
@Service("discountService")
public class DiscountServiceImpl extends BaseServiceImpl<Discount> implements DiscountService {

	@Resource
	private DiscountDao discountDao;
	@Override
	public List<Discount> queryByPage(int currentPage, int pageSize, String qusername, String qpassword) {
		Map<String,Object> map=new HashMap<>();
        map.put("currentPage", (currentPage-1)*pageSize);
        map.put("pageSize", pageSize);
        map.put("qusername", qusername);
        map.put("qpassword", qpassword);
		return discountDao.queryByPage(map);
	}
	@Override
	public int getTotals(String qusername, String qpassword) {
		Map<String,Object> map=new HashMap<>();
        map.put("qusername", qusername);
        map.put("qpassword", qpassword);
		return discountDao.getTotals(map);
	}

}
